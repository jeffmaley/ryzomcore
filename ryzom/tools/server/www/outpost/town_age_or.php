        <b>L'&acirc;ge d'or des avant-postes</b></p>
      <p><br>
        Chez les Trykers, puis plus tard chez les autres peuples, des marchands 
        et des artisans profit&egrave;rent de la pr&eacute;sence des foreuses 
        pour s'installer &agrave; proximit&eacute;, construisant de v&eacute;ritables 
        petits villages dans les r&eacute;gions sauvages. Les avant-postes se 
        d&eacute;velopp&egrave;rent, &eacute;tendant leurs activit&eacute;s au-del&agrave; 
        de l'extraction des ressources. Des comptoirs commerciaux ou diplomatiques, 
        des forteresses, des postes fronti&egrave;res et m&ecirc;me des centres 
        de recherche apparurent. Suite aux recherches des Sculpteurs de Vase, 
        les artisans des nouvelles terres apprirent &agrave; utiliser certains 
        mat&eacute;riaux trait&eacute;s par les foreuses pour cr&eacute;er des 
        objets avec de nouvelles particularit&eacute;s. Ce fut l'&acirc;ge d'or 
        des avant-postes.<br>
        Rilano Coruti, un cartographe d'origine matis, membre de la guilde des 
        Gardiens de l'Atrium, dressa une carte de plus de cent cinquante avant-postes 
        et leur donna &agrave; chacun un nom en fonction de leur activit&eacute;. 
        Ces noms sont toujours utilis&eacute;s aujourd'hui, m&ecirc;me si ces 
        activit&eacute;s ont disparu. Car apr&egrave;s l'&acirc;ge d'or vint le 
        d&eacute;clin des avant-postes.</p>
